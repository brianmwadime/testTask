export const getTokenForSymbol = (symbol: string): string => {
  if (logosBySymbol[symbol]) {
    return logosBySymbol[symbol.toUpperCase()];
  } else if (symbol?.toLowerCase() === 'dexshare') {
    return dexShare;
  } else {
    return unknownLogo;
  }
};
